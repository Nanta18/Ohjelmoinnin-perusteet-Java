public class EX09_02 {
    public static void main(String[] args) {
        int i = 1;
        for (int j = 0; j < 10; j++) {
            int doubled = i * 2;
            System.out.println(i + " times two equals " + doubled);
            i++;
        }
        System.out.println("End of program");
    }
}
